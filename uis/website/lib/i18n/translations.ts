export type Lang = 'en' | 'es';

export type TranslationKey = keyof typeof translations.en;

export type FormErrorKey = keyof typeof translations.en.errors;

export type UiTranslationKey = {
  [K in TranslationKey]: (typeof translations.en)[K] extends string ? K : never;
}[TranslationKey];

export function translate(lang: Lang, key: UiTranslationKey): string {
  return translations[lang][key];
}

export function formErrors(lang: Lang, field: FormErrorKey): string {
  return translations[lang].errors[field];
}
